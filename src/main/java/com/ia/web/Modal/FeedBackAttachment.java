package com.ia.web.Modal;

public class FeedBackAttachment {

	private int feedbackId;
	private int feedbackAttachmentId;
	private String filePath;
	
	
	public int getFeedbackAttachmentId() {
		return feedbackAttachmentId;
	}
	public void setFeedbackAttachmentId(int feedbackAttachmentId) {
		this.feedbackAttachmentId = feedbackAttachmentId;
	}
	public int getFeedbackId() {
		return feedbackId;
	}
	public void setFeedbackId(int feedbackId) {
		this.feedbackId = feedbackId;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	
	
		 
}
