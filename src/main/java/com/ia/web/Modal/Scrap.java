package com.ia.web.Modal;

public class Scrap {
	
	private int scrapId;
	private String name;
	private String current_org;
	private String current_position;
	private String location;
	private String url;
	public int getScrapId() {
		return scrapId;
	}
	public void setScrapId(int scrapId) {
		this.scrapId = scrapId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCurrent_org() {
		return current_org;
	}
	public void setCurrent_org(String current_org) {
		this.current_org = current_org;
	}
	public String getCurrent_position() {
		return current_position;
	}
	public void setCurrent_position(String current_position) {
		this.current_position = current_position;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	} 

	
	
}
