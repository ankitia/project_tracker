package com.ia.web.Modal;

public class EmailConversion {

	private int projectId;
	private int emailConversionId;
	private String emailLog;
	private int createdBy;
	private String createdDate;
	private String userName;
	private String fullName;
	private String filePath;
	
	
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	public int getProjectId() {
		return projectId;
	}
	public void setProjectId(int projectId) {
		this.projectId = projectId;
	}
	public int getEmailConversionId() {
		return emailConversionId;
	}
	public void setEmailConversionId(int emailConversionId) {
		this.emailConversionId = emailConversionId;
	}
	public String getEmailLog() {
		return emailLog;
	}
	public void setEmailLog(String emailLog) {
		this.emailLog = emailLog;
	}
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public int getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(int createdBy) {
		this.createdBy = createdBy;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	
	
	 
}
