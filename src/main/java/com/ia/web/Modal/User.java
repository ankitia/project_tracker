package com.ia.web.Modal;

public class User {
	
	private String userName;
	private String serverIp;
	private String password;
	private int limit;
	private String fname;
	private String lname;
	private int userId;
	private int usageLimit;
	private int pendingRequest;
	private int userRoleId;
	
	
	/**
	 * @return the userRoleId
	 */
	public int getUserRoleId() {
		return userRoleId;
	}
	/**
	 * @param userRoleId the userRoleId to set
	 */
	public void setUserRoleId(int userRoleId) {
		this.userRoleId = userRoleId;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	/**
	 * @return the pendingRequest
	 */
	public int getPendingRequest() {
		return pendingRequest;
	}
	/**
	 * @param pendingRequest the pendingRequest to set
	 */
	public void setPendingRequest(int pendingRequest) {
		this.pendingRequest = pendingRequest;
	}
	/**
	 * @return the usageLimit
	 */
	public int getUsageLimit() {
		return usageLimit;
	}
	/**
	 * @param usageLimit the usageLimit to set
	 */
	public void setUsageLimit(int usageLimit) {
		this.usageLimit = usageLimit;
	}
	/**
	 * @return the userId
	 */
	public int getUserId() {
		return userId;
	}
	/**
	 * @param userId the userId to set
	 */
	public void setUserId(int userId) {
		this.userId = userId;
	}
	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}
	/**
	 * @param userName the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	/**
	 * @return the serverIp
	 */
	public String getServerIp() {
		return serverIp;
	}
	/**
	 * @param serverIp the serverIp to set
	 */
	public void setServerIp(String serverIp) {
		this.serverIp = serverIp;
	}
	/**
	 * @return the limit
	 */
	public int getLimit() {
		return limit;
	}
	/**
	 * @param limit the limit to set
	 */
	public void setLimit(int limit) {
		this.limit = limit;
	}
	/**
	 * @return the fname
	 */
	public String getFname() {
		return fname;
	}
	/**
	 * @param fname the fname to set
	 */
	public void setFname(String fname) {
		this.fname = fname;
	}
	/**
	 * @return the lname
	 */
	public String getLname() {
		return lname;
	}
	/**
	 * @param lname the lname to set
	 */
	public void setLname(String lname) {
		this.lname = lname;
	}
	
	
	
	
	
}
